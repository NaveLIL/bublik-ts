// ═══════════════════════════════════════════════
//  1. Приветственное сообщение (публичное, в канале)
// ═══════════════════════════════════════════════

export function buildWelcomeEmbed(member: GuildMember): BublikEmbed {
  return new BublikEmbed()
    .setColor(COLOR_WELCOME)
    .setAuthor({
      name: `${Config.botName} — Добро пожаловать!`,
      iconURL: member.guild.iconURL({ size: 64 }) ?? undefined,
    })
    .setThumbnail(member.user.displayAvatarURL({ size: 256 }))
    .setDescription(
      `Приветствуем, <@${member.id}>! 👋\n\n` +
      `Рады видеть вас на сервере **${member.guild.name}**!\n` +
      `Подскажите, вы к нам по вопросу **вступления в полк** или по **другому вопросу**?\n\n` +
      `> Пожалуйста, выберите подходящий вариант ниже 👇`,
    );
}

