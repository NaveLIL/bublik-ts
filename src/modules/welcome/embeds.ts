// ═══════════════════════════════════════════════
//  2. Промпт ознакомления с правилами (ephemeral)
// ═══════════════════════════════════════════════

export function buildRulesPromptEmbed(
  serverRead: boolean,
  regimentRead: boolean,
): BublikEmbed {
  const sIcon = serverRead ? '✅' : '❌';
  const rIcon = regimentRead ? '✅' : '❌';
  const sText = serverRead ? '*прочитано*' : '*не прочитано*';
  const rText = regimentRead ? '*прочитано*' : '*не прочитано*';

  const allRead = serverRead && regimentRead;

  let description =
    'Рады, что вы сделали такой выбор, **кандидат**! 🎖️\n\n' +
    'Перед тем как подать тикет на вступление в полк, вам необходимо ' +
    'ознакомиться с **правилами нашего полка**.\n\n' +
    '⚠️ **Кнопка «Ознакомился» станет доступна после прочтения обоих документов.**';

  if (allRead) {
    description +=
      '\n\n' +
      '─────────────────────────────\n' +
      '✨ **Отлично!** Вы ознакомились с обоими документами.\n' +
      'Нажмите кнопку **«Ознакомился с правилами»** чтобы продолжить!';
  }

  return new BublikEmbed()
    .setColor(COLOR_RECRUIT)
    .setAuthor({ name: 'EREZ — Вступление в полк' })
    .setDescription(description)
    .addFields({
      name: '📋 Статус ознакомления',
      value: [
        `> ${sIcon} Правила сервера — ${sText}`,
        `> ${rIcon} Правила полка — ${rText}`,
      ].join('\n'),
    });
}

